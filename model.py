class Generator(nn.Module):
    """Generator network."""
    def __init__(self, conv_dim=64, c_dim=5, repeat_num=6):
        super(Generator, self).__init__()

        layers = []
        layers.append(nn.Conv2d(3+c_dim, conv_dim, kernel_size=7, stride=1, padding=3, bias=False))
        # layers.append(nn.InstanceNorm2d(conv_dim, affine=True, track_running_stats=True))
        layers.append(nn.ReLU(inplace=True))

        # Down-sampling layers.
        curr_dim = conv_dim
        for i in range(2):
            layers.append(nn.Conv2d(curr_dim, curr_dim*2, kernel_size=4, stride=2, padding=1, bias=False))
            # layers.append(nn.InstanceNorm2d(curr_dim*2, affine=True, track_running_stats=True))
            layers.append(nn.ReLU(inplace=True))
            curr_dim = curr_dim * 2

        # Bottleneck layers.
        for i in range(repeat_num):
            layers.append(ResidualBlock(dim_in=curr_dim, dim_out=curr_dim))

        # Up-sampling layers.
        for i in range(2):
            layers.append(nn.ConvTranspose2d(curr_dim, curr_dim//2, kernel_size=4, stride=2, padding=1, bias=False))
            layers.append(nn.InstanceNorm2d(curr_dim//2, affine=True, track_running_stats=True))
            layers.append(nn.ReLU(inplace=True))
            curr_dim = curr_dim // 2

        layers.append(nn.Conv2d(curr_dim, 3, kernel_size=7, stride=1, padding=3, bias=False))
        layers.append(nn.Tanh())
        self.main = nn.Sequential(*layers)

    def forward(self, x, c):  #c: torch.Size([16, 3])
        # Replicate spatially and concatenate domain information.
        # Note that this type of label conditioning does not work at all if we use reflection padding in Conv2d.
        # This is because instance normalization ignores the shifting (or bias) effect.
        c = c.view(c.size(0), c.size(1), 1, 1)   #c: torch.Size([16, 3, 1, 1])
        c = c.repeat(1, 1, x.size(2), x.size(3)) # torch.Size([16, 3, 128, 128])
        x = torch.cat([x, c], dim=1)       # torch.Size([16, 6, 128, 128])

        return self.main(x)

class StyleGenerator(nn.Module):
    def __init__(self, conv_dim=64, c_dim=3, repeat_num=6):
        super(StyleGenerator, self).__init__()

        down_layers, res_layers, up_layers = [],[],[]
        down_layers.append(nn.Conv2d(3, conv_dim, kernel_size=7, stride=1, padding=3, bias=False))
        down_layers.append(nn.ReLU(inplace=True))

        # Down-sampling layers.
        curr_dim = conv_dim
        for i in range(2):
            down_layers.append(nn.Conv2d(curr_dim, curr_dim*2, kernel_size=3, stride=2, padding=1, bias=False))
            down_layers.append(nn.ReLU(inplace=True))
            curr_dim = curr_dim * 2

        # Bottleneck layers.
        # res_layers.append(nn.ConvTranspose2d(curr_dim+3, curr_dim, kernel_size=3, stride=1, padding=1, bias=False))
        for i in range(repeat_num):
            res_layers.append(ResidualBlock(dim_in=curr_dim, dim_out=curr_dim))

        # Up-sampling layers.
        for i in range(2):
            up_layers.append(nn.ConvTranspose2d(curr_dim, curr_dim//2, kernel_size=4, stride=2, padding=1, bias=False))
            up_layers.append(AdaptiveInstanceNorm2d(curr_dim//2))
            up_layers.append(nn.ReLU(inplace=True))
            curr_dim = curr_dim // 2

        up_layers.append(nn.Conv2d(curr_dim, 3, kernel_size=7, stride=1, padding=3, bias=False))
        up_layers.append(nn.Tanh())

        self.down_layers = nn.Sequential(*down_layers)
        self.res_layers = nn.Sequential(*res_layers)
        self.up_layers = nn.Sequential(*up_layers)

        # MLP to generate AdaIN parameters
        self.mlp = MLP(c_dim, self.get_num_adain_params(self.up_layers), 256, 3, norm='none', activ='lrelu')


    def assign_adain_params(self, adain_params, model):
        # assign the adain_params to the AdaIN layers in model
        for m in model.modules():
            if m.__class__.__name__ == "AdaptiveInstanceNorm2d":
                mean = adain_params[:, :m.num_features]
                std = adain_params[:, m.num_features:2*m.num_features]
                m.bias = mean.contiguous().view(-1)
                m.weight = std.contiguous().view(-1)
                if adain_params.size(1) > 2*m.num_features:
                    adain_params = adain_params[:, 2*m.num_features:]

    def get_num_adain_params(self, model):
        # return the number of AdaIN parameters needed by the model
        num_adain_params = 0
        for m in model.modules():
            if m.__class__.__name__ == "AdaptiveInstanceNorm2d":
                num_adain_params += 2*m.num_features
        return num_adain_params

    def forward(self, x, c, content_consistent=False):  #c: torch.Size([16, 3])
        # 1. adain
        down = self.down_layers(x)  # torch.Size([16, 256, 64, 64])
        adain_params = self.mlp(c)
        self.assign_adain_params(adain_params, self.up_layers)
        
        # resblock
        feats = self.res_layers(down) 
                   
        up = self.up_layers(feats)
        if content_consistent:
            return up, down
        else:
            return up




class Discriminator(nn.Module):
    """Discriminator network with PatchGAN."""
    def __init__(self, image_size=128, conv_dim=64, c_dim=5, repeat_num=6):
        super(Discriminator, self).__init__()
        layers = []
        layers.append(nn.Conv2d(3, conv_dim, kernel_size=4, stride=2, padding=1))
        layers.append(nn.LeakyReLU(0.01))

        curr_dim = conv_dim
        for i in range(1, repeat_num):
            layers.append(nn.Conv2d(curr_dim, curr_dim*2, kernel_size=4, stride=2, padding=1))
            layers.append(nn.LeakyReLU(0.01))
            curr_dim = curr_dim * 2

        kernel_size = int(image_size / np.power(2, repeat_num))
        self.main = nn.Sequential(*layers)
        self.conv1 = nn.Conv2d(curr_dim, 1, kernel_size=3, stride=1, padding=1, bias=False)
        self.conv2 = nn.Conv2d(curr_dim, c_dim, kernel_size=kernel_size, bias=False)
        
    def forward(self, x):
        # print(x.shape,'----------') #torch.Size([16, 128, 128, 3]) should be torch.Size([16, 3, 128, 128])
        h = self.main(x)
        out_src = self.conv1(h)
        out_cls = self.conv2(h)
        return out_src, out_cls.view(out_cls.size(0), out_cls.size(1))
       
class MsImageDis(nn.Module):

    # Multi-scale discriminator architecture
    def __init__(self, opt=None):
        super(MsImageDis, self).__init__()
        self.n_layer = 4
        self.dim = 64
        self.norm = 'none'
        self.activ = 'lrelu'
        self.num_scales = 3
        self.pad_type = 'reflect'
        self.input_dim = 3
        self.class_block_use = True
        self.class_nums = opt.c_dim + 1
        self.downsample = nn.AvgPool2d(3, stride=2, padding=[1, 1], count_include_pad=False)
        self.cnns = nn.ModuleList()
        for _ in range(self.num_scales):
            self.cnns.append(self._make_net())

    def _make_net(self):
        dim = self.dim
        cnn_x = []
        cnn_x += [Conv2dBlock(self.input_dim, dim, 4, 2, 1, norm='none', activation=self.activ, pad_type=self.pad_type)]
        for i in range(self.n_layer - 1):
            cnn_x += [Conv2dBlock(dim, dim * 2, 4, 2, 1, norm=self.norm, activation=self.activ, pad_type=self.pad_type)]
            dim *= 2
        
        if self.class_block_use:
            cnn_x += [nn.Conv2d(dim, self.class_nums, 4, 1, 1)]   
        else:
            cnn_x += [nn.Conv2d(dim, 2, 4, 1, 1)]   

        cnn_x1 = nn.Sequential(*cnn_x)

        return cnn_x1

    def forward(self, x):
        outputs = []
        for cnn_x in self.cnns:
            outputs.append(cnn_x(x))
            x = self.downsample(x)   
        return outputs



